package pw.dedominic.airc.helper;

import android.os.Handler;

import org.pircbotx.PircBotX;
import org.pircbotx.exception.IrcException;

import java.io.IOException;

/**
 * Thread which handles running startbot()
 * and sends disconnected event if it stops
 */
public class BotThread extends Thread{

    private PircBotX connection;
    private Handler handler;

    /**
     * Allocates a new {@code Thread} object. This constructor has the same
     * effect as {@linkplain #Thread(ThreadGroup, Runnable, String) Thread}
     * {@code (null, null, gname)}, where {@code gname} is a newly generated
     * name. Automatically generated names are of the form
     * {@code "Thread-"+}<i>n</i>, where <i>n</i> is an integer.
     */
    public BotThread(PircBotX connection, Handler handler) {
        this.connection = connection;
        this.handler = handler;
    }

    /**
     * If this thread was constructed using a separate
     * <code>Runnable</code> run object, then that
     * <code>Runnable</code> object's <code>run</code> method is called;
     * otherwise, this method does nothing and returns.
     * <p>
     * Subclasses of <code>Thread</code> should override this method.
     *
     * @see #start()
     * @see #stop()
     * @see #Thread(ThreadGroup, Runnable, String)
     */
    @Override
    public void run() {
        try {
            connection.startBot();
        } catch (Exception e) {
            e.printStackTrace();
        }
        handler.sendEmptyMessage(ConnectionListener.DISCONNECTED_EVENT);
    }
}
